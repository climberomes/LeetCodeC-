//pointer approach with checking apla numeric as we go
class Solution {
public:
    bool isPalindrome(string s) {
        int left = 0, right = s.size()-1;
        while(left<right)
        {
            if(!isalnum(s[left]))
            {
                left++;
            }
            else if(!isalnum(s[right]))
            {
                right--;
            }
            else if (tolower(s[left])==tolower(s[right]))
            {
                left++;
                right--;
            }
            else
            {
                return false;
            }
        }
        return true;
    }
};
